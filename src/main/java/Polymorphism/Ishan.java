package Polymorphism;

public class Ishan {
    public static void main(String[] args) {
        System.out.println("Hello");
        display(1);
    }
    static{
        System.out.println("My Name is Ishan");
    }
    public static void display(int n){
        System.out.println("This is my method no. :" +n);
    }
}
